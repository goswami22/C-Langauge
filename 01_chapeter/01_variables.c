#include <stdio.h>

int main() {
    int a;  // Declare an integer variable 'a'
    int b;
    a = 3;  // Assign the value 3 to 'a'  - initialization  
    b = 4;
    printf("The value of a is: %d\n", a);
    printf("The value of b is: %d\n", b);
    printf("The sum of a and b is: %d\n", a + b);

    return 0;
}